# CLI
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description="Review a GitHub PR and produce structured Markdown output."
    )
    parser.add_argument(
        "--pr", required=True,
        help="PR URL (https://github.com/owner/repo/pull/123) or owner/repo/number"
    )
    parser.add_argument(
        "--output", "-o",
        help="Output file path (default: stdout)"
    )
    parser.add_argument(
        "--format", choices=["markdown", "json"], default="markdown",
        help="Output format (default: markdown)"
    )
    parser.add_argument(
        "--no-fallback", action="store_true",
        help="Fail if Claude API is not available instead of using heuristic analysis"
    )
    args = parser.parse_args()

    # Parse PR
    try:
        owner, repo, pr_number = parse_pr_url(args.pr)
    except ValueError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

    pr_url = f"https://github.com/{owner}/{repo}/pull/{pr_number}"

    # Fetch PR info and diff
    print(f"🔍 Fetching PR #{pr_number} from {owner}/{repo}...", file=sys.stderr)
    try:
        pr_info = fetch_pr_info(owner, repo, pr_number)
        diff = fetch_pr_diff(owner, repo, pr_number)
    except RuntimeError as e:
        print(f"Error fetching PR: {e}", file=sys.stderr)
        sys.exit(1)

    if not diff:
        print("Error: PR has no diff (empty or already merged)", file=sys.stderr)
        sys.exit(1)

    diff_size = len(diff)
    file_count = len([l for l in diff.split("\n") if l.startswith("diff --git")])
    print(f"📄 Diff size: {diff_size:,} bytes across {file_count} files",
          file=sys.stderr)

    # Analyze
    print("🧠 Analyzing...", file=sys.stderr)
    has_api = bool(os.environ.get("ANTHROPIC_API_KEY"))
    if not has_api and args.no_fallback:
        print("Error: ANTHROPIC_API_KEY not set and --no-fallback specified",
              file=sys.stderr)
        sys.exit(1)

    if has_api:
        print("🤖 Using Claude API for analysis", file=sys.stderr)
    else:
        print("⚙️  ANTHROPIC_API_KEY not set, using heuristic analysis", file=sys.stderr)
        print("💡 Set ANTHROPIC_API_KEY for AI-powered reviews", file=sys.stderr)

    result = analyze_with_claude(diff, pr_info)
    review = PRReview.from_dict(result, pr_url)

    # Output
    output = review.to_markdown() if args.format == "markdown" else review.to_json()

    if args.output:
        with open(args.output, "w", encoding="utf-8") as f:
            f.write(output)
        print(f"✅ Review written to {args.output}", file=sys.stderr)
    else:
        print(output)

    sys.exit(0)


if __name__ == "__main__":
    main()
